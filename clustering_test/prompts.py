"""Промпт для кластеризации.

Формат ответа модели — компактный двухчастный JSON для скорости:
{
  "clusters": {"1": "имя_кластера_1", "2": "имя_кластера_2", ...},
  "assignments": {"1": 1, "2": 5, "3": 2, ...}
}

Это сжимает output в ~3 раза vs старого формата {"1": "имя_кластера", ...}
поскольку имя кластера повторяется N раз для N хвостов вместо одного.
"""

SYSTEM_PROMPT_TEMPLATE = """Ты группируешь поисковые запросы по интенту пользователя.

Тебе передан сид (тема), регион поиска, язык запросов и список хвостов \
(ключи с вычтенными словами сида). Хвосты могут содержать латиницу \
(бренды, англицизмы) — это нормально, обрабатывай их как часть интента.

Сначала придумай список интент-кластеров под этот конкретный сид и хвосты. \
Какие кластеры реально нужны для ЭТОГО сида — такие и создавай. \
Никакого жёсткого списка нет.

Названия кластеров: короткие (1-3 слова) на {language}, нижний регистр, \
подчёркивания вместо пробелов. Количество — сколько реально нужно для этих хвостов \
(обычно 8-15).

Затем каждому хвосту присвой ровно один кластер по его номеру.

Формат ответа — JSON:
{{
  "clusters": {{"1": "имя_первого_кластера", "2": "имя_второго", ...}},
  "assignments": {{"1": 1, "2": 5, "3": 2, ...}}
}}

Где:
- ключи в "clusters" — номера кластеров (строки), значения — имена кластеров
- ключи в "assignments" — номера хвостов (строки), значения — номера кластеров (числа)

Только JSON, без пояснений, без markdown-обёрток."""


USER_PROMPT_TEMPLATE = """Сид: {seed}
Регион: {region}
Язык запросов: {language}

Хвосты ({n}):
{payloads_text}"""


def build_prompts(seed: str, region: str, language: str, payloads: list[str]) -> tuple[str, str]:
    payloads_text = '\n'.join(
        f'{i+1}. {p if p else "(пусто)"}'
        for i, p in enumerate(payloads)
    )
    system = SYSTEM_PROMPT_TEMPLATE.format(language=language)
    user = USER_PROMPT_TEMPLATE.format(
        seed=seed,
        region=region,
        language=language,
        n=len(payloads),
        payloads_text=payloads_text,
    )
    return system, user
