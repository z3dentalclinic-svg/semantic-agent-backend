"""Промпт для кластеризации."""

SYSTEM_PROMPT_TEMPLATE = """Ты группируешь поисковые запросы по интенту пользователя.

Тебе передан сид (тема), регион поиска, язык запросов и список хвостов \
(ключи с вычтенными словами сида). Хвосты могут содержать латиницу \
(бренды, англицизмы) — это нормально, обрабатывай их как часть интента.

Базовые интент-кластеры:
- гео_город — название города
- гео_район — район/микрорайон
- гео_область — область/регион
- гео_страна — страна
- цена — стоимость, дёшево, недорого
- вопросительный — как, где, почему, что такое
- срочность — сегодня, 24/7, к дате, праздники
- бренд_сервис — название магазина/маркетплейса/сети
- ассортимент — типы товаров, категории
- условия — способ, формат, опции
- коммерция — заказать/купить
- отзывы — отзывы, рейтинг
- проблемы — мошенничество, возврат, жалобы
- работа — поиск работы по теме
- прочее — не подошло никуда

Если из хвостов виден специфический интент темы которого нет в списке — \
добавь свой кластер с понятным названием (1-3 слова на {language}).

Каждому хвосту присвой ровно один интент.

Сначала составь список используемых интентов. Каждому интенту присвой букву-метку: \
A, B, C, D, ... Если интентов больше 26 — продолжай AA, AB, AC, ...

Ответ строго в двух блоках, разделённых строкой `---`:

Блок 1: маппинг меток на имена интентов, по одному на строке:
A=гео_город
B=цена
C=бренд_сервис

Блок 2: после `---` одна строка из меток через запятую, длина равна числу хвостов. \
Позиция = номер хвоста (1-я метка для хвоста №1, 2-я для №2, и т.д.).

Пример полного ответа на 7 хвостов и 3 интента:
A=гео_город
B=цена
C=бренд_сервис
---
A,B,C,A,B,A,C

Только этот формат. Без пояснений, без markdown-обёрток, без пустых строк внутри блоков."""


USER_PROMPT_TEMPLATE = """Сид: {seed}
Регион: {region}
Язык запросов: {language}

Хвосты ({n}):
{payloads_text}"""


def build_prompts(seed: str, region: str, language: str, payloads: list[str]) -> tuple[str, str]:
    payloads_text = '\n'.join(
        f'{i+1}. {p if p else "(пусто)"}'
        for i, p in enumerate(payloads)
    )
    system = SYSTEM_PROMPT_TEMPLATE.format(language=language)
    user = USER_PROMPT_TEMPLATE.format(
        seed=seed,
        region=region,
        language=language,
        n=len(payloads),
        payloads_text=payloads_text,
    )
    return system, user
